package com.a11032023;

public class DatatypeVarEx {
	
	public static void main (String[]args)
	
	{
		//Datatype variable = value;
	
	     int num = 9;
	     int num2 = 8;
	     
	     int ans = num + num2;
	     int ans1 = num - num2;
	     int ans2 = num * num2;
	     int ans3 = num/num2;
	     
	     System.out.println(ans);
	     System.out.println(ans1);
	     System.out.println(ans2);
	     System.out.println(ans3);
	     
	
	
	} 
}
