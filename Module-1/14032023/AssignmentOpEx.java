package com.a14032023;

public class AssignmentOpEx {
	
	
	public static void main(String[] args) {
		
	
	
	int a = 60;
	int b = 30;
	
	a+=b;//a=a+b	
	System.out.println(a);
	
	
	a-=b;//a=a-b
	System.out.println(a);
	
	
	a*=b;//a=a*b
	System.out.println(a);
	
	a/=b;//a=a/b
	System.out.println(a);
	
	a%=b;//a=a%b
	System.out.println(a);
	
	
	}

}
