package com.a14032023;

public class ArithmeticOperatorEx {
	
	
	public static void main(String[] args) {
		
		
		int a = 60;
		int b = 30;
		
		int add = a+b;
		int sub = a-b;
		int mul = a*b;
		int div = a/b;
		int mod = a%b;
		
		
		System.out.println(add);
		System.out.println(sub);
		System.out.println(mul);
		System.out.println(div);
		System.out.println(mod);
		
		
		
	}

}
